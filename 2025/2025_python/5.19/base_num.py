"""
2進数、8進数、16進数
"""

# 2進数 binary
print(0b0001) ### 1
print(0b0010) ### 2
print(0b0011) ### 3
print(0b0100) ### 4
print(0b0101) ### 5
print(0b0110) ### 6
print(0b0111) ### 7
print(0b1000) ### 8
print(0b1001) ### 9
print(0b1010) ### 10
print(0b1011) ### 11
print(0b1100) ### 12
print(0b1101) ### 13
print(0b1110) ### 14
print(0b1111) ### 15

# 8進数 octal
print(0o07) ### 7
print(0o10) ### 8
print(0o11) ### 9
print(0o12) ### 10
print(0o20) ### 16　

# 16進数 hexadecimal
print(0x09) ### 9
print(0x0a) ### 10
print(0x0b) ### 11
print(0x0c) ### 12
print(0x0d) ### 13
print(0x0e) ### 14
print(0x0f) ### 15
print(0x10) ### 16
