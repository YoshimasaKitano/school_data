""" 問題3
数字パズル ― 条件一致で正しい3桁の数字を導出

[目的]
3桁の数字 abc（各桁は整数 a,b,c）で、指定された複数条件をすべて満たす組み合わせを探索する。

[入力仕様]
a, b, c：それぞれ 1～9 の整数値（桁ごとに一意で昇順）

[条件]
a + b + c = 12
a × b × c = 36
a < b < c（昇順）
3桁の数字 abc は3の倍数である（例：123）

[出力仕様]
条件をすべて満たす 3桁の整数を1つ出力
最初に見つかった候補で終了
見つからなかった場合は「条件を満たす組み合わせは存在しません。」と出力

[制約]
list構造、ユーザー定義関数の使用禁止
for文による全探索で対応
"""
for a in range(1, 8):
    for b in range(2, 9):
        for c in range(3, 10):
            if (a + b + c == 12) and (a * b * c == 36) and (int(str(a) + str(b) + str(c)) % 3 == 0):
                print(f"見つかりました: {str(a) + str(b) + str(c)}")
                break
            else:
                if a == 7 and b == 8 and c == 9:
                    print("条件を満たす組み合わせは存在しません。")
                    break