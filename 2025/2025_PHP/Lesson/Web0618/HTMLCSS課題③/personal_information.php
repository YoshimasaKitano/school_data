        <h2>個人情報保護法について</h2>
        <section>
            <h3 class="personal_text">第一条</h3>
            <p class="personal_text">この法律は、高度情報通信社会の進展に伴い個人情報の利用が著しく拡大していることにかんがみ、個人情報の適正な取扱いに関し、基本理念及び政府による基本方針の作成その他の個人情報の保護に関する施策の基本となる事項を定め、国及び地方公共団体の責務等を明らかにするとともに、個人情報を取り扱う事業者の遵守すべき義務等を定めることにより、個人情報の有用性に配慮しつつ、個人の権利利益を保護することを目的とする。</p>
        </section>
        <section>
            <h3 class="personal_text">第二条</h3>
            <p class="personal_text">1 この法律において「個人情報」とは、生存する個人に関する情報であって、当該情報に含まれる氏名、生年月日その他の記述等により特定の個人を識別することができるもの（他の情報と容易に照合することができ、それにより特定の個人を識別することができることとなるものを含む。）をいう。<br>2 この法律において「個人情報データベース等」とは、個人情報を含む情報の集合物であって、次に掲げるものをいう。<br> 一 特定の個人情報を電子計算機を用いて検索することができるように体系的に構成したもの <br>二 前号に掲げるもののほか、特定の個人情報を容易に検索することができるように体系的に構成したものとして政令で定めるもの<br> この法律において「個人情報取扱事業者」とは、個人情報データベース等を事業の用に供している者をいう。ただし、次に掲げる者を除く。<br>一	 国の機関<br>二 地方公共団体<br>三 独立行政法人等（独立行政法人等の保有する個人情報の保護に関する法律（平成十五年法律第五十九号）第二条第一項に規定する独立行政法人等をいう。以下同じ。）<br>四 地方独立行政法人（地方独立行政法人法（平成十五年法律第百十八号）第二条第一項に規定する地方独立行政法人をいう。以下同じ。）<br>五 その取り扱う個人情報の量及び利用方法からみて個人の権利利益を害するおそれが少ないものとして政令で定める者<br>4 この法律において「個人データ」とは、個人情報データベース等を構成する個人情報をいう。<br>5 この法律において「保有個人データ」とは、個人情報取扱事業者が、開示、内容の訂正、追加又は削除、利用の停止、消去及び第三者への提供の停止を行うことのできる権限を有する個人データであって、その存否が明らかになることにより公益その他の利益が害されるものとして政令で定めるもの又は一年以内の政令で定める期間以内に消去することとなるもの以外のものをいう。<br>6 この法律において個人情報について「本人」とは、個人情報によって識別される特定の個人をいう。（基本理念）</p>
        </section>
        <section>
            <h3 class="personal_text">第三条</h3>
            <p class="personal_text">個人情報は、個人の人格尊重の理念の下に慎重に取り扱われるべきものであることにかんがみ、その適正な取扱いが図られなければならない。</p>
        </section>
        <section>
            <h3 class="personal_text">第四条</h3>
            <p class="personal_text">国は、この法律の趣旨にのっとり、個人情報の適正な取扱いを確保するために必要な施策を総合的に策定し、及びこれを実施する責務を有する。（地方公共団体の責務）</p>
        </section>
        <section>
            <h3 class="personal_text">第五条</h3>
            <p class="personal_text">地方公共団体は、この法律の趣旨にのっとり、その地方公共団体の区域の特性に応じて、個人情報の適正な取扱いを確保するために必要な施策を策定し、及びこれを実施する責務を有する。（法制上の措置等）</p>
        </section>
        <section>
            <h3 class="personal_text">第六条</h3>
            <p class="personal_text">1 政府は、国の行政機関について、その保有する個人情報の性質、当該個人情報を保有する目的等を勘案し、その保有する個人情報の適正な取扱いが確保されるよう法制上の措置その他必要な措置を講ずるものとする。<br>2 政府は、独立行政法人等について、その性格及び業務内容に応じ、その保有する個人情報の適正な取扱いが確保されるよう法制上の措置その他必要な措置を講ずるものとする。<br>3 政府は、前二項に定めるもののほか、個人情報の性質及び利用方法にかんがみ、個人の権利利益の一層の保護を図るため特にその適正な取扱いの厳格な実施を確保する必要がある個人情報について、保護のための格別の措置が講じられるよう必要な法制上の措置その他の措置を講ずるものとする。</p>
        </section>
        <section>
            <h3 class="personal_text">第七条</h3>
            <p class="personal_text">1 政府は、個人情報の保護に関する施策の総合的かつ一体的な推進を図るため、個人情報の保護に関する基本方針（以下「基本方針」という。）を定めなければならない。<br>2 基本方針は、次に掲げる事項について定めるものとする。<br>一 個人情報の保護に関する施策の推進に関する基本的な方向<br>二	 国が講ずべき個人情報の保護のための措置に関する事項<br>三 地方公共団体が講ずべき個人情報の保護のための措置に関する基本的な事項<br>四 独立行政法人等が講ずべき個人情報の保護のための措置に関する基本的な事項<br>五	 地方独立行政法人が講ずべき個人情報の保護のための措置に関する基本的な事項<br>六 個人情報取扱事業者及び第四十条第一項に規定する認定個人情報保護団体が講ずべき個人情報の保護のための措置に関する基本的な事項<br>七 個人情報の取扱いに関する苦情の円滑な処理に関する事項<br>八 その他個人情報の保護に関する施策の推進に関する重要事項<br>3 内閣総理大臣は、国民生活審議会の意見を聴いて、基本方針の案を作成し、閣議の決定を求めなければならない。<br>4 内閣総理大臣は、前項の規定による閣議の決定があったときは、遅滞なく、基本方針を公表しなければならない。<br>5 前二項の規定は、基本方針の変更について準用する。</p>
        </section>
        <section>
            <h3 class="personal_text">第八条</h3>
            <p class="personal_text">国は、地方公共団体が策定し、又は実施する個人情報の保護に関する施策及び国民又は事業者等が個人情報の適正な取扱いの確保に関して行う活動を支援するため、情報の提供、事業者等が講ずべき措置の適切かつ有効な実施を図るための指針の策定その他の必要な措置を講ずるものとする。（苦情処理のための措置）</p>
        </section>
        <section>
            <h3 class="personal_text">第九条</h3>
            <p class="personal_text">国は、個人情報の取扱いに関し事業者と本人との間に生じた苦情の適切かつ迅速な処理を図るために必要な措置を講ずるものとする。（個人情報の適正な取扱いを確保するための措置）</p>
        </section>
        <section>
            <h3 class="personal_text">第十条</h3>
            <p class="personal_text">国は、地方公共団体との適切な役割分担を通じ、次章に規定する個人情報取扱事業者による個人情報の適正な取扱いを確保するために必要な措置を講ずるものとする。</p>
        </section>
        <section>
            <h3 class="personal_text">第十一条</h3>
            <p class="personal_text">1 地方公共団体は、その保有する個人情報の性質、当該個人情報を保有する目的等を勘案し、その保有する個人情報の適正な取扱いが確保されるよう必要な措置を講ずることに努めなければならない。<br>2 地方公共団体は、その設立に係る地方独立行政法人について、その性格及び業務内容に応じ、その保有する個人情報の適正な取扱いが確保されるよう必要な措置を講ずることに努めなければならない。（区域内の事業者等への支援）</p>
        </section>
        <section>
            <h3 class="personal_text">第十二条</h3>
            <p class="personal_text">地方公共団体は、個人情報の適正な取扱いを確保するため、その区域内の事業者及び住民に対する支援に必要な措置を講ずるよう努めなければならない。（苦情の処理のあっせん等）</p>
        </section>
        <section>
            <h3 class="personal_text">第十三条</h3>
            <p class="personal_text">地方公共団体は、個人情報の取扱いに関し事業者と本人との間に生じた苦情が適切かつ迅速に処理されるようにするため、苦情の処理のあっせんその他必要な措置を講ずるよう努めなければならない。</p>
        </section>
        <section>
            <h3 class="personal_text">第十四条</h3>
            <p class="personal_text">国及び地方公共団体は、個人情報の保護に関する施策を講ずるにつき、相協力するものとする。</p>
        </section>
        <section>
            <h3 class="personal_text">第十五条</h3>
            <p class="personal_text">1 個人情報取扱事業者は、個人情報を取り扱うに当たっては、その利用の目的（以下「利用目的」という。）をできる限り特定しなければならない。<br>2 個人情報取扱事業者は、利用目的を変更する場合には、変更前の利用目的と相当の関連性を有すると合理的に認められる範囲を超えて行ってはならない。（利用目的による制限）</p>
        </section>
        <section>
            <h3 class="personal_text">第十六条</h3>
            <p class="personal_text">1 個人情報取扱事業者は、あらかじめ本人の同意を得ないで、前条の規定により特定された利用目的の達成に必要な範囲を超えて、個人情報を取り扱ってはならない。<br>2 個人情報取扱事業者は、合併その他の事由により他の個人情報取扱事業者から事業を承継することに伴って個人情報を取得した場合は、あらかじめ本人の同意を得ないで、承継前における当該個人情報の利用目的の達成に必要な範囲を超えて、当該個人情報を取り扱ってはならない。<br>3 前二項の規定は、次に掲げる場合については、適用しない。<br>一 法令に基づく場合<br>二 人の生命、身体又は財産の保護のために必要がある場合であって、本人の同意を得ることが困難であるとき。<br>三 公衆衛生の向上又は児童の健全な育成の推進のために特に必要がある場合であって、本人の同意を得ることが困難であるとき。<br>四 国の機関若しくは地方公共団体又はその委託を受けた者が法令の定める事務を遂行することに対して協力する必要がある場合であって、本人の同意を得ることにより当該事務の遂行に支障を及ぼすおそれがあるとき。（適正な取得）</p>
        </section>
        <section>
            <h3 class="personal_text">第十七条</h3>
            <p class="personal_text">個人情報取扱事業者は、偽りその他不正の手段により個人情報を取得してはならない。（取得に際しての利用目的の通知等）</p>
        </section>
        <section>
            <h3 class="personal_text">第十八条</h3>
            <p class="personal_text">1 個人情報取扱事業者は、個人情報を取得した場合は、あらかじめその利用目的を公表している場合を除き、速やかに、その利用目的を、本人に通知し、又は公表しなければならない。<br>2 個人情報取扱事業者は、前項の規定にかかわらず、本人との間で契約を締結することに伴って契約書その他の書面（電子的方式、磁気的方式その他人の知覚によっては認識することができない方式で作られる記録を含む。以下この項において同じ。）に記載された当該本人の個人情報を取得する場合その他本人から直接書面に記載された当該本人の個人情報を取得する場合は、あらかじめ、本人に対し、その利用目的を明示しなければならない。ただし、人の生命、身体又は財産の保護のために緊急に必要がある場合は、この限りでない。<br>3 個人情報取扱事業者は、利用目的を変更した場合は、変更された利用目的について、本人に通知し、又は公表しなければならない。<br>4 前三項の規定は、次に掲げる場合については、適用しない。<br>一 利用目的を本人に通知し、又は公表することにより本人又は第三者の生命、身体、財産その他の権利利益を害するおそれがある場合<br>二 利用目的を本人に通知し、又は公表することにより当該個人情報取扱事業者の権利又は正当な利益を害するおそれがある場合<br>三 国の機関又は地方公共団体が法令の定める事務を遂行することに対して協力する必要がある場合であって、利用目的を本人に通知し、又は公表することにより当該事務の遂行に支障を及ぼすおそれがあるとき。<br>四	 取得の状況からみて利用目的が明らかであると認められる場合（データ内容の正確性の確保）</p>
        </section>
        <section>
            <h3 class="personal_text">第十九条</h3>
            <p class="personal_text">個人情報取扱事業者は、利用目的の達成に必要な範囲内において、個人データを正確かつ最新の内容に保つよう努めなければならない。（安全管理措置）</p>
        </section>
        <section>
            <h3 class="personal_text">第二十条</h3>
            <p class="personal_text">個人情報取扱事業者は、その取り扱う個人データの漏えい、滅失又はき損の防止その他の個人データの安全管理のために必要かつ適切な措置を講じなければならない。（従業者の監督）</p>
        </section>
        <section>
            <h3 class="personal_text">第二十一条</h3>
            <p class="personal_text">個人情報取扱事業者は、その従業者に個人データを取り扱わせるに当たっては、当該個人データの安全管理が図られるよう、当該従業者に対する必要かつ適切な監督を行わなければならない。（委託先の監督）</p>
        </section>
        <section>
            <h3 class="personal_text">第二十二条</h3>
            <p class="personal_text">個人情報取扱事業者は、個人データの取扱いの全部又は一部を委託する場合は、その取扱いを委託された個人データの安全管理が図られるよう、委託を受けた者に対する必要かつ適切な監督を行わなければならない。（第三者提供の制限）</p>
        </section>
        <section>
            <h3 class="personal_text">第二十三条</h3>
            <p class="personal_text">1 個人情報取扱事業者は、次に掲げる場合を除くほか、あらかじめ本人の同意を得ないで、個人データを第三者に提供してはならない。<br>一 法令に基づく場合<br>二 人の生命、身体又は財産の保護のために必要がある場合であって、本人の同意を得ることが困難であるとき。<br>三 公衆衛生の向上又は児童の健全な育成の推進のために特に必要がある場合であって、本人の同意を得ることが困難であるとき。<br>四 国の機関若しくは地方公共団体又はその委託を受けた者が法令の定める事務を遂行することに対して協力する必要がある場合であって、本人の同意を得ることにより当該事務の遂行に支障を及ぼすおそれがあるとき。<br>2 個人情報取扱事業者は、第三者に提供される個人データについて、本人の求めに応じて当該本人が識別される個人データの第三者への提供を停止することとしている場合であって、次に掲げる事項について、あらかじめ、本人に通知し、又は本人が容易に知り得る状態に置いているときは、前項の規定にかかわらず、当該個人データを第三者に提供することができる。<br>一 第三者への提供を利用目的とすること。<br>二	 第三者に提供される個人データの項目<br>三	 第三者への提供の手段又は方法<br>四 本人の求めに応じて当該本人が識別される個人データの第三者への提供を停止すること。<br>3 個人情報取扱事業者は、前項第二号又は第三号に掲げる事項を変更する場合は、変更する内容について、あらかじめ、本人に通知し、又は本人が容易に知り得る状態に置かなければならない。<br>4 次に掲げる場合において、当該個人データの提供を受ける者は、前三項の規定の適用については、第三者に該当しないものとする。<br>一 個人情報取扱事業者が利用目的の達成に必要な範囲内において個人データの取扱いの全部又は一部を委託する場合<br>二 合併その他の事由による事業の承継に伴って個人データが提供される場合<br>三 個人データを特定の者との間で共同して利用する場合であって、その旨並びに共同して利用される個人データの項目、共同して利用する者の範囲、利用する者の利用目的及び当該個人データの管理について責任を有する者の氏名又は名称について、あらかじめ、本人に通知し、又は本人が容易に知り得る状態に置いているとき。<br>5 個人情報取扱事業者は、前項第三号に規定する利用する者の利用目的又は個人データの管理について責任を有する者の氏名若しくは名称を変更する場合は、変更する内容について、あらかじめ、本人に通知し、又は本人が容易に知り得る状態に置かなければならない。（保有個人データに関する事項の公表等）</p>
        </section>
        <section>
            <h3 class="personal_text">第二十四条</h3>
            <p class="personal_text">1 個人情報取扱事業者は、保有個人データに関し、次に掲げる事項について、本人の知り得る状態（本人の求めに応じて遅滞なく回答する場合を含む。）に置かなければならない。<br>一 当該個人情報取扱事業者の氏名又は名称<br>二 すべての保有個人データの利用目的（第十八条第四項第一号から第三号までに該当する場合を除く。）<br>三 次項、次条第一項、第二十六条第一項又は第二十七条第一項若しくは第二項の規定による求めに応じる手続（第三十条第二項の規定により手数料の額を定めたときは、その手数料の額を含む。）<br>四 前三号に掲げるもののほか、保有個人データの適正な取扱いの確保に関し必要な事項として政令で定めるもの<br>2 個人情報取扱事業者は、本人から、当該本人が識別される保有個人データの利用目的の通知を求められたときは、本人に対し、遅滞なく、これを通知しなければならない。ただし、次の各号のいずれかに該当する場合は、この限りでない。<br>一 前項の規定により当該本人が識別される保有個人データの利用目的が明らかな場合<br>二 第十八条第四項第一号から第三号までに該当する場合<br>3 個人情報取扱事業者は、前項の規定に基づき求められた保有個人データの利用目的を通知しない旨の決定をしたときは、本人に対し、遅滞なく、その旨を通知しなければならない。（開示）</p>
        </section>
